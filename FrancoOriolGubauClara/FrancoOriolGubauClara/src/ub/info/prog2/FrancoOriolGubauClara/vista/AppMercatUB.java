    private void itmnCarregarFitxerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_itmnCarregarFitxerActionPerformed
        try {
            JFileChooser fc = new JFileChooser();
            int resultat = fc.showOpenDialog(this);
            if (resultat == JFileChooser.APPROVE_OPTION) {
                controlador.carregarMercat(fc.getSelectedFile().getAbsolutePath());
            }
        }catch (MercatException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(),"", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_itmnCarregarFitxerActionPerformed

    private void itmnGuardarFitxerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_itmnGuardarFitxerActionPerformed
        try {
            String nomFitxer = JOptionPane.showInputDialog(this, "Introdueixi la ruta per guardar el fitxer","", JOptionPane.DEFAULT_OPTION);
            controlador.guardarMercat(nomFitxer);
        } catch (MercatException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(),"", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_itmnGuardarFitxerActionPerformed

